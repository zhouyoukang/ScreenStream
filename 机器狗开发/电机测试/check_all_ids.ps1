# 测试特定电机ID
function Test-MotorId {
    param (
        [int]$motorId,
        [int]$baudRate = 5000000
    )
    
    Write-Host "测试电机ID: $motorId, 波特率: $baudRate" -ForegroundColor Cyan
    
    try {
        # 尝试打开串口
        $port = New-Object System.IO.Ports.SerialPort("COM5", $baudRate, "None", 8, "One")
        $port.ReadTimeout = 500
        $port.WriteTimeout = 500
        
        Write-Host "打开COM5端口..." -ForegroundColor Yellow
        $port.Open()
        Write-Host "COM5端口打开成功" -ForegroundColor Green
        
        # 获取命令
        $cmd1 = Get-PositionCommand1 $motorId
        $cmd2 = Get-PositionCommand2 $motorId
        
        # 发送两个方向的命令各5次
        for ($i = 1; $i -le 5; $i++) {
            Write-Host "  发送位置命令1 (尝试 $i/5)..." -ForegroundColor Yellow
            $port.Write($cmd1, 0, $cmd1.Length)
            Start-Sleep -Milliseconds 500
            
            Write-Host "  发送位置命令2 (尝试 $i/5)..." -ForegroundColor Yellow
            $port.Write($cmd2, 0, $cmd2.Length)
            Start-Sleep -Milliseconds 500
        }
        
        # 关闭端口
        $port.Close()
        Write-Host "COM5端口关闭" -ForegroundColor Green
        return $true
    } 
    catch {
        Write-Host "错误: $_" -ForegroundColor Red
        try {
            if ($port -and $port.IsOpen) {
                $port.Close()
                Write-Host "COM5端口已关闭" -ForegroundColor Green
            }
        } catch {}
        return $false
    }
}

